uint32_t initial_encoder_count = 0; // 초기 엔코더 위치
uint32_t current_encoder_count = 0; // 현재 엔코더 카운트 값
float angle = 0.0f;       // 계산된 각도


// 엔코더 카운트 값을 360도로 변환하는 함수
float calculate_angle(uint32_t current_count, uint32_t initial_count)
{
    // 카운트 차이 계산
    int32_t count_diff = current_count - initial_count;

    // 0에서 360도 범위로 변환
    // 카운트 차이를 MAX_ENCODER_COUNT로 나누어 360도를 곱함
    float angle = (float)count_diff / (float)MAX_ENCODER_COUNT * 360.0f;

    // 부호에 따라 범위 조정
    if (angle < 0)
    {
        angle += 360.0f;  // 360도 내로 값을 맞추기 위해 양수로 처리
    }

    return angle;
}




